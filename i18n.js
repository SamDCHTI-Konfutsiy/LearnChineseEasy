function getLang(){
  try{
    const saved = localStorage.getItem(I18N_LANG_KEY);
    if(saved && I18N[saved]) return saved;
  }catch(e){}
  return 'uz';
}
function t(key){
  const lang = getLang();
  return (I18N[lang] && I18N[lang][key]) || (I18N.uz && I18N.uz[key]) || key;
}
function setLang(lang){
  if(!I18N[lang]) return;
  try{ localStorage.setItem(I18N_LANG_KEY, lang); }catch(e){}
  applyTranslations();
  if(typeof onLanguageChanged === 'function') onLanguageChanged();
}
function applyTranslations(){
  const lang = getLang();
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const val = t(el.getAttribute('data-i18n'));
    if(val) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el=>{
    const val = t(el.getAttribute('data-i18n-placeholder'));
    if(val) el.placeholder = val;
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(el=>{
    const val = t(el.getAttribute('data-i18n-aria'));
    if(val) el.setAttribute('aria-label', val);
  });
  renderLangSwitches();
}
function renderLangSwitches(){
  const lang = getLang();
  document.querySelectorAll('.lang-switch').forEach(container=>{
    container.innerHTML = '';
    I18N_LANGS.forEach(l=>{
      const btn = document.createElement('button');
      btn.type = 'button';
      btn.className = 'lang-btn' + (l.code===lang ? ' active' : '');
      btn.textContent = l.label;
      btn.addEventListener('click', ()=> setLang(l.code));
      container.appendChild(btn);
    });
  });
}
document.addEventListener('DOMContentLoaded', applyTranslations);
